def solution(n):
    def DFS(queens, xy_dif, xy_sum):
        p = len(queens)
        if p==n:
            result.append(queens)
            return None
        for q in range(n):
            # xy_dif keeps track of all the invalid right diagonals
            # xy_sum keeps track of all the invalid left diagonals
            if q not in queens and p-q not in xy_dif and p+q not in xy_sum: 
                DFS(queens+[q], xy_dif+[p-q], xy_sum+[p+q])  
    result = []
    DFS([],[],[])
    return [ [i+1 for i in sol] for sol in result]
    